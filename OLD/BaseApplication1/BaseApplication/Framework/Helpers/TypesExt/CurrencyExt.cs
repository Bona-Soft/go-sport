namespace MYB.BaseApplication.Framework.Helpers.TypesExt
{
   public static class CurrencyExt
   {
      /// <summary>
      ///   Argentine Austral
      /// </summary>
      public static string AsAustralSimbol(this decimal dec)
      {
         return "=A= " + dec.ToString();
      }

      /// <summary>
      ///   Baht tailandés / balboa panameño
      /// </summary>
      public static string AsBalboaSimbol(this decimal dec)
      {
         return "฿ " + dec.ToString();
      }

      /// <summary>
      ///    Venezuelan Bolívar
      /// </summary>
      public static string AsBahtSimbol(this decimal dec)
      {
         return "฿ " + dec.ToString();
      }

      /// <summary>
      ///   Venezuelan Bolívar
      /// </summary>
      public static string AsBolivarSimbol(this decimal dec)
      {
         return "Bs " + dec.ToString();
      }

      /// <summary>
      ///   Venezuelan Bs.F.Bolívar fuerte 
      /// </summary>
      public static string AsBolivarFuerteSimbol(this decimal dec)
      {
         return "Bs.F. " + dec.ToString();
      }

      /// <summary>
      ///   Cedi ghanés
      /// </summary>
      public static string AsCediSimbol(this decimal dec)
      {
         return "GH₵ " + dec.ToString();
      }

      /// <summary>
      ///   American Dolar
      /// </summary>
      public static string AsDolarSimbol(this decimal dec)
      {
         return "U$S " + dec.ToString();
      }

      /// <summary>
      ///   Argentine Peso
      /// </summary>
      public static string AsPesoArgSimbol(this decimal dec)
      {
         return "$ " + dec.ToString();
      }

      /// <summary>
      ///   Adds the simbol
      /// </summary>
      public static string AsSimbol(this decimal dec, string simbol)
      {
         return simbol + " " + dec.ToString();
      }
   }
}